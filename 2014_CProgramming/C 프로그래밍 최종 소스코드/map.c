void Map_CharMapInit(Map *mp)
{
	int i, j;

	for (i = 0; i < 300; i++)
	{
		for (j = 0; j < 300; j++)
		{
			mp->cMap[i][j] = '\0';
		}
	}

	for (i = 0; i < mp->height; i++)
	{
		for (j = 0; j < mp->width; j++)
		{
			if (mp->pMap[i][j] > 0)
			{
				if (mp->pMap[i][j] == 1 || mp->pMap[i][j] == 2)
				{
					strcat(mp->cMap[i], WALL_FIGURE);
				}
				else
				{
					strcat(mp->cMap[i], GATE_FIGURE);
				}
			}
			else
			{
				strcat(mp->cMap[i], "  ");
			}
		}
	}
}

